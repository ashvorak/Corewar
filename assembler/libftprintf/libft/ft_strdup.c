#include <stdlib.h>

char	*ft_strdup(const char *src)
{
	int		i;
	int		len;
	char	*res;

	len = 0;
	i = 0;
	while (src[len])
		len++;
	if (!(res = (char*)malloc(sizeof(char) * (len + 1))))
		return (NULL);
	while (src[i])
	{
		res[i] = src[i];
		i++;
	}
	res[i] = '\0';
	return (res);
}
